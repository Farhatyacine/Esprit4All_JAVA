package models;

/**
 *
 * @author Sami
 */
public class Membre {

    private Club club;
    private User user;
    private String role;

    public Membre(Club club, User user, String role) {
        this.club = club;
        this.user = user;
        this.role = role;
    }

    public Club getClub() {
        return club;
    }

    public void setClub(Club club) {
        this.club = club;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

}
